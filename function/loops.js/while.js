// estrutura de repetição while

// while significa enquanto a condição for verdadeira. Observação, devemos ter cuidado para não virar um loop infinito e podendo assim travar o computador

let i = 0

while(i <= 10) {
    console.log(`Observe que ainda eu sou menor que 10`)

    i++
}

// no while a variável fica fora do bloco, dentro do while temos a condição e oq a engine do js vai fazer e sua incrementação

